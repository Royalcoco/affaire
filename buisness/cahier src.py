import hashlib
import time

class Block:
    def __init__(self, index, previous_hash, timestamp, data, nonce):
        self.index = index
        self.previous_hash = previous_hash
        self.timestamp = timestamp
        self.data = data
        self.nonce = nonce
        self.hash = self.calculate_hash()

    def calculate_hash(self):
        data = str(self.index) + self.previous_hash + str(self.timestamp) + str(self.data) + str(self.nonce)
        return hashlib.sha256(data.encode()).hexdigest()

class Blockchain:
    def __init__(self):
        self.chain = [self.create_genesis_block()]
        self.difficulty = 4

    def create_genesis_block(self):
        return Block(0, "0", time.time(), "Genesis Block", 0)

    def get_latest_block(self):
        return self.chain[-1]

    def add_block(self, new_block):
        if self.is_valid_proof(new_block):
            self.chain.append(new_block)

    def is_valid_proof(self, block):
        return block.hash[:self.difficulty] == '0' * self.difficulty

    def mine_block(self, data):
        previous_block = self.get_latest_block()
        new_block_index = previous_block.index + 1
        new_block_timestamp = time.time()
        new_block_nonce = 0
        new_block = Block(new_block_index, previous_block.hash, new_block_timestamp, data, new_block_nonce)

        while not self.is_valid_proof(new_block):
            new_block_nonce += 1
            new_block.timestamp = time.time()
            new_block.hash = new_block.calculate_hash()

        self.add_block(new_block)
        return new_block

